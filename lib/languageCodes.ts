const codeLookup = new Map(
  supportedCodes.map((code) => [code.toLowerCase(), code])
);

export function resolveLanguageCode(code?: string | null) {
  if (!code) return null;
  return codeLookup.get(code.trim().toLowerCase()) ?? null;
}

export function isSupportedLanguageCode(code?: string | null) {
  return resolveLanguageCode(code) !== null;
}

export function ensureSupportedLanguageCode(code?: string | null, fallback = "en") {
  return resolveLanguageCode(code) ?? fallback;
}

